/**
 * #4 :: Export JS function "isGreaterThan" to check whether one number is greater than a given number
 * input: number (value)
 * input: number (threshold)
 * output: boolean
 */

/**
 * returns whether one number is greater than a given number
 * @param {number} firstNumber
 * @param {number} secondNumber
 * @returns {boolean}
 */
const isGreaterThan = function (firstNumber, secondNumber) {
    return firstNumber > secondNumber;
};

export default isGreaterThan;
